PROCEDURE SpawnHunterWithM16;
VAR
	hunter : EntityPtr;
	
	temp : string[16];
	theHunter : string[32];
	hnum : string[4];

BEGIN
	{ Generate Hunter name using BaseName and num }
	IntegerToString(hnum, num);
	StringCat(temp, BaseName, hnum);
	StringCat(theHunter, temp, '(hunter)');
	
	{ Spawn Hunter }
	hunter := SpawnMovingEntity(HunterType, pos, theHunter);
	if hunter <> nil then writedebug(theHunter,' added') else writedebug('not added');
	

	AIAddEntity(theHunter);

	AISetIdleHomeNode(GetEntityName(hunter), '');
	
	
	AIEntityIgnoreDeadBodies(GetEntityName(hunter), true);
	CreateInventoryItem(CT_COLT_COMMANDO, hunter, true);
	SetHunterDropAmmo(hunter, -1);
	AISetHunterIdleActionMinMaxRadius(GetEntityName(hunter), AISCRIPT_IDLE_WANDERSEARCH, AISCRIPT_HIGHPRIORITY, 10, 18, 20.0);
			
	AIAddHunterToLeaderSubpack(LeaderName, SubPackName, theHunter);
	
	
	{SpawnHunter := hunter;}
END;


