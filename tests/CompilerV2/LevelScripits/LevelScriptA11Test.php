		AIAddHunterToLeaderSubpack('leader(leader)', subpack, name);
	end;
end;

procedure DestroyAllHuntersInSubpack;
var
	NrHunters, OldNrHunters : integer;
	Success : boolean;
	HunterName : string[30];

begin
	WriteDebug('DestroyAllHunters in ', SubpackName);

    { Get initial number of hunters in subpack }
    NrHunters := AINumberInSubpack(LeaderName, SubpackName);

    { Are there any hunters left in this subpack ? }
	while NrHunters > 0 do
	begin
        { Get name of first hunter in subpack }
		Success := AIReturnSubpackEntityName(LeaderName, SubpackName, 1, HunterName);
		if Success = TRUE then
		begin
		    { Destroy this hunter }
            DestroyEntity(GetEntity(HunterName));

            { Wait until number of hunters in subpack changes }
            OldNrHunters := NrHunters;
            while NrHunters = OldNrHunters do
            begin
                sleep(50);
                NrHunters := AINumberInSubpack(LeaderName, SubpackName);
            end;
		end;
	end;
end;

