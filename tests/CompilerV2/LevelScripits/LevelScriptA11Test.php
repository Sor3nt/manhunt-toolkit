PROCEDURE SetDoorStatus; { (var Door : String, State : Boolean); }
var
	pdoor	:	EntityPtr;
begin
	pdoor := GetEntity(Door);

	if pdoor <> NIL then
	begin
		if State = FALSE then
		begin
			UnfreezeEntity(pdoor);
			LockEntity(pdoor);
			GraphModifyConnections(pdoor, AISCRIPT_GRAPHLINK_ALLOW_NOTHING);
		end
		else
		begin
			UnfreezeEntity(pdoor);
			UnlockEntity(pdoor);
			GraphModifyConnections(pdoor, AISCRIPT_GRAPHLINK_ALLOW_EVERYTHING);
		end;
	end;
end;

